<script>
import axios from 'axios';
export default {
  data() {
    return {
      users: []
    };
  },
  mounted() {
    this.fetchUsers();
  },
  methods: {
    fetchUsers() {
      axios.get('http://bocafast.io/controller/usersController.php?action=getAllUsers')
        .then(res => {
          console.log(res)
          this.users = res.data;
        })
        .catch(err => {
          console.log(err)
        })
    }
  }
}
</script>

<template>
  <header>
    <img alt="Vue logo" class="logo" src="./assets/logo.svg" width="125" height="125" />

    <div class="wrapper">
    </div>
  </header>

  <main>
    <table>
      <thead>
        <tr>
          <th>id</th>
          <th>username</th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="user in users" :key="user.id">
          <td>{{ user.id }}</td>
          <td>{{ user.username }}</td>
        </tr>
      </tbody>
    </table>
  </main>
</template>

